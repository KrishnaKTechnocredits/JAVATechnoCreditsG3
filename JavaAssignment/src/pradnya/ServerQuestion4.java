package pradnya;

public class ServerQuestion4 {

	public int areaRectangle(int l, int w)
	{
		int a = l*w;
		return a;
	}
	public int perimeterOfRectangle(int l, int w)
	{
		int p = 2*(l+w);
		return p;
	}
	public float areaCircle(float r)
	{
		float f = 3.14f;
		float a = f*(r*r);
		return a;
	}
	public float perimeterOfCircle(float r)
	{
		float f = 3.14f;
		float c = 2*f*r;
		return c;
	}
}
